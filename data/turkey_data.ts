export const getDistrictsForCity = (cityName: string): string[] => {
  const districts = TURKEY_DISTRICTS[cityName] || ["Merkez"];
  // Türkçe karakter duyarlı sıralama
  return districts.sort((a, b) => {
      // Merkez her zaman en başta
      if (a === "Merkez") return -1;
      if (b === "Merkez") return 1;
      return a.localeCompare(b, 'tr');
  });
};
